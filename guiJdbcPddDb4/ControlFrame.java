    private DefaultTableModel buildTableModel(String query, String rowsCount){
        int count = 0;
        try {
            Statement st = con.createStatement();
            ResultSet countRs = st.executeQuery(rowsCount);
            if(countRs.next()){
                count = countRs.getInt(1);
            }
            countRs.close();
            ResultSet rs = st.executeQuery(query);
            ResultSetMetaData metaData = rs.getMetaData();
//--------------------------массив с именами столбцов---------------------------
            Object[] object = new Object[metaData.getColumnCount()];
            for(int i = 0; i < object.length; i++){
                object[i] = metaData.getColumnName(i + 1);
            }
//------------------------------------------------------------------------------
            //ArrayList<Object[]> arrayList = new ArrayList<Object[]>();
            Object[][] multiDimArray = new Object[count][metaData.getColumnCount()];
                for(int i = 0; i < multiDimArray.length; i++){
                    rs.next();
                    for(int j = 0; j < multiDimArray[i].length; j++){
                        multiDimArray[i][j] = rs.getString(j + 1);
                    }
                }
                st.close();
                return new DefaultTableModel(
                        multiDimArray,
                        object);
        } catch (SQLException ex) {
            System.out.println("Error: "+ ex.getMessage());
            Logger.getLogger(ControlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
