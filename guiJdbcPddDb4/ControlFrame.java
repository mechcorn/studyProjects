    private void findQuestionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findQuestionButtonActionPerformed
        try {
            // TODO add your handling code here:
            Dimension size = questionPictureLabel.getSize();
            questionId.delete(0, questionId.length());
            questionId.append('t');
            questionId.append(ticketComboBox.getSelectedItem());
            questionId.append('q');
            questionId.append(questionComboBox.getSelectedItem());
            questionPs = con.prepareStatement(queryQuestion);
            questionPs.setString(1, questionId.toString());
            ResultSet rs = questionPs.executeQuery();
            
            questWrAnsPs = con.prepareStatement(wrAnsQuery);
            questWrAnsPs.setString(1, questionId.toString());
            ResultSet wrAnsRs = questWrAnsPs.executeQuery();
            
            if(rs.next()){
                questionContentTextArea.setText(rs.getString("question_content"));
                questionRightAnswerTextField.setText(rs.getString("right_answer"));
                Image image = new ImageIcon(rs.getBytes("question_picture")).getImage();
                questionPictureLabel.setIcon(new ImageIcon(image.getScaledInstance(size.width, size.height, Image.SCALE_FAST)));
            }
            rs.close();
            
            columnNames = new Vector<Object>();
            ResultSetMetaData metaData = wrAnsRs.getMetaData();
            int columnCount = wrAnsRs.getMetaData().getColumnCount();
            for(int i = 1; i <= columnCount; i++){
                columnNames.add(metaData.getColumnName(i));
            }
            wrAnsOneQuest = new Vector<Vector<Object>>();
            while(wrAnsRs.next()){
                Vector<Object> vector = new Vector<Object>();
                for(int i = 1; i <= columnCount; i++){
                    vector.add(wrAnsRs.getObject(i));
                }
                wrAnsOneQuest.add(vector);
            }
            wrAnsRs.close();
            questionWrongAnswersTable.setModel(new DefaultTableModel(wrAnsOneQuest, columnNames));
            
            rewriteQuestionButton.setEnabled(true);
            
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
            Logger.getLogger(ControlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_findQuestionButtonActionPerformed

