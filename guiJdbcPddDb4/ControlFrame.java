    private void rewriteQuestionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rewriteQuestionButtonActionPerformed
        // TODO add your handling code here:
        int dialogResult = JOptionPane.showConfirmDialog(
                null,
                "Вы уверены?",
                "Предупреждение",
                JOptionPane.YES_NO_CANCEL_OPTION);
        switch(dialogResult){
            case JOptionPane.YES_OPTION:
        {
            try {
                //обновим информацию в таблице questions
                if(fullFileName != null){
                    updateQuestionsPs = con.prepareStatement(updateQuestions);
                    File file = new File(fullFileName);
                    FileInputStream fis = new FileInputStream(file);
                    updateQuestionsPs.setBinaryStream(3, (InputStream)fis, (int)file.length());
                    updateQuestionsPs.setString(4, questionId.toString());
                    
                }else{
                    updateQuestionsPs = con.prepareStatement(updateQuestionsWithoutPicture);
                    
                    updateQuestionsPs.setString(3, questionId.toString());
                }
                updateQuestionsPs.setString(1, questionContentTextArea.getText().trim());
                updateQuestionsPs.setString(2, questionRightAnswerTextField.getText().trim());
                updateQuestionsPs.execute();
                //обновим информацию в таблице wrong_answers
                Statement st = con.createStatement();
                String deleteAllPriorWrongAnswers = "DELETE FROM Wrong_answers WHERE question_id='" + questionId.toString() + "'";
                st.execute(deleteAllPriorWrongAnswers);
                st.close();
                
                insertWrAnsPs = con.prepareStatement(insertSubsequentWrongAnswers);
                int rowCount = questionWrongAnswersTable.getModel().getRowCount();
                for(int i = 0; i < rowCount; i++){
                    insertWrAnsPs.setString(1, questionId.toString());
                    String str = (String)questionWrongAnswersTable.getModel().getValueAt(i, 0);
                    if(str != null && !str.equals("")){
                        insertWrAnsPs.setString(2, str);
                        insertWrAnsPs.execute();
                    }
                }
                JOptionPane.showMessageDialog(null, "Вопрос " + questionId.toString() + " изменён");
            } catch (SQLException | FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
                Logger.getLogger(ControlFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
                break;
            default:
                break;
        }
    }//GEN-LAST:event_rewriteQuestionButtonActionPerformed

